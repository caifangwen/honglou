-- 本地开发环境权限初始化脚本
-- 在 00-local-roles.sql 之后执行

-- 1. 授予 anon 角色所有表的访问权限
GRANT ALL ON ALL TABLES IN SCHEMA public TO anon;
GRANT ALL ON ALL SEQUENCES IN SCHEMA public TO anon;

