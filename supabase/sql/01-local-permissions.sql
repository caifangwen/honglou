ALTER TABLE intel_trades DISABLE ROW LEVEL SECURITY;
ALTER TABLE intel_intercepts DISABLE ROW LEVEL SECURITY;
ALTER TABLE messages DISABLE ROW LEVEL SECURITY;
ALTER TABLE rumors DISABLE ROW LEVEL SECURITY;
ALTER TABLE ledger_entries DISABLE ROW LEVEL SECURITY;
ALTER TABLE treasury DISABLE ROW LEVEL SECURITY;
ALTER TABLE steward_accounts DISABLE ROW LEVEL SECURITY;
ALTER TABLE steward_stamina DISABLE ROW LEVEL SECURITY;
ALTER TABLE maid_relationships DISABLE ROW LEVEL SECURITY;
ALTER TABLE servant_relationships DISABLE ROW LEVEL SECURITY;
ALTER TABLE relationships DISABLE ROW LEVEL SECURITY;
ALTER TABLE action_approvals DISABLE ROW LEVEL SECURITY;
ALTER TABLE allowance_records DISABLE ROW LEVEL SECURITY;
ALTER TABLE procurement_tickets DISABLE ROW LEVEL SECURITY;

